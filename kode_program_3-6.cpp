#include <iostream>
using namespace std;

int main (){
    //mendeklarasikan variabel x (diisi nilai 10) dan y (diisi nilai 3)
    int x =10, y = 3 ;
    int z;//mendeklarasikan variabel z sebagai penampung nilai hasil operasi

    //melakukan operasi pengurangan
    z = x-y ;

    //menampilkan hasil penjumlahan 
    cout<<x<<" - "<<y<<" = "<<z;

    return 0;
}